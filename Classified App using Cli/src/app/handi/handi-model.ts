export class Dish {
    dishName: string;
    dishImage: string;
    dishPrice: number;
    dishQuantity: string;

    constructor(dishName: string, dishImage: string, dishPrice: number, dishQuantity: string) {
        this.dishName = dishName;
        this.dishImage = dishImage;
        this.dishQuantity = dishQuantity;
        this.dishPrice = dishPrice;


    }

}